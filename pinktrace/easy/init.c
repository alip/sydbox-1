#include <pinktrace/easy/private.h>
#include <pinktrace/easy/pink.h>

#include <stdbool.h>
#include <signal.h>
#include <sys/utsname.h>

unsigned pink_easy_os_release;

/* FIXME: Weird function that returns 0 on error */
static unsigned get_os_release(void)
{
	unsigned rel;
	const char *p;
	struct utsname u;
	if (uname(&u) < 0) {
		/* perror_msg_and_die("uname"); */
		return 0;
	}
	/* u.release has this form: "3.2.9[-some-garbage]" */
	rel = 0;
	p = u.release;
	for (;;) {
		if (!(*p >= '0' && *p <= '9')) {
			/* error_msg_and_die("Bad OS release string: '%s'", u.release); */
			return 0;
		}
		/* Note: this open-codes KERNEL_VERSION(): */
		rel = (rel << 8) | atoi(p);
		if (rel >= KERNEL_VERSION(1,0,0))
			break;
		while (*p >= '0' && *p <= '9')
			p++;
		if (*p != '.') {
			if (rel >= KERNEL_VERSION(0,1,0)) {
				/* "X.Y-something" means "X.Y.0" */
				rel <<= 8;
				break;
			}
			/* error_msg_and_die("Bad OS release string: '%s'", u.release); */
			return 0;
		}
		p++;
	}
	return rel;
}

bool pink_easy_init(void)
{
	pink_easy_os_release = get_os_release();
	if (pink_easy_os_release == 0)
		return false;
	return true;
}
